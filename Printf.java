import java.util.*;

public class Printf{
       public static void main(String[] args){
              String str="hello world";
              System.out.printf("%s",str);
              System.out.printf("\n%s",new String("This is a new String"));
              System.out.printf("\n\t%s\n",new Integer(64));
              System.out.println(new String("This's another"));
       }
}